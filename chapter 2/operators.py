# Arithmetic Operators
x = 14
y = 3

addition = x+y
substraction = x-y
multiplication = x*y
division = x/y
modulus = x%y
exponentiation = x**y

print("Addition is: ", addition)
print("Substraction is: ", substraction)
print("Multiplication is: ", multiplication)
print("Division is: ", division)
print("Modulus is: ", modulus)
print("Exponentiation is: ", exponentiation)

# Comparison Operators
print (x==y) #false
print (x!=y) #true
print (x>y) #true
print (x<y) #false
print (x>=y) #true
print (x<=y) #false

# Logical Operators
a = True
b = False
print (a and b) #false
print (a or b) #true
print (not a) #false

# Assignment Operators
x += 5
print (x) #19
x -= 2
print (x) #17
x *= 3
print (x) #51
x /= 2
print (x) #25.5